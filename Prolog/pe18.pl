controller(FIRSTPART, LASTPART, LENGTH, ANS) :-
	LENGTH > 1,
	NEWLENGTH is LENGTH - 1,
	lastNitems(FIRSTPART, ENDARRAY, LASTN, NEWLENGTH),
	specialSum(LASTN, LASTPART, NEW),
	controller(ENDARRAY, NEW, NEWLENGTH, ANS).

specialSum([],_, []).
specialSum([SS | SHORTEND],[LF | LONG], NEW) :-
		LONG = [LS | LONGEND],
		LF > LS,
		ITEM is LF + SS,
		specialSum(SHORTEND, LONG, NEWNEW),
		append([ITEM], NEWNEW, NEW).
specialSum([SS | SHORTEND],[LF | LONG], NEW) :-
		LONG = [LS | LONGEND],
		LF < LS,
		ITEM is LS + SS,
		specialSum(SHORTEND, LONG, NEWNEW),
		append([ITEM], NEWNEW, NEW).

lastNitems(STARTARRAY, STARTARRAY, [], 0).
lastNitems(STARTARRAY, ENDARRAY, LASTNITEMS, N) :-
	N > 0,
	M is N - 1,
	append(NEWSTARTARRAY, [LASTITEM], STARTARRAY),
	lastNitems(NEWSTARTARRAY, ENDARRAY, NEWLASTNITEMS, M),
	append(NEWLASTNITEMS, [LASTITEM], LASTNITEMS).